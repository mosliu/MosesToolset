    private void jToggleButton_StartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton_StartActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        portAddress1 = getserialport1();
        portAddress2 = getserialport2();

        if (handler1 == null) {
            handler1 = new BridgeHandler();
            ((BridgeHandler) handler1).name = "1";
        }
        if (handler2 == null) {
            handler2 = new BridgeHandler();
            ((BridgeHandler) handler2).name = "2";
        }
        if (receiver1 != null) {
            receiver1.endListen();
        }
        if (receiver2 != null) {
            receiver2.endListen();
        }

        receiver1 = new RS232Connector(handler1, portAddress1);
        receiver2 = new RS232Connector(handler2, portAddress2);

        ((BridgeHandler) handler1).setDest(receiver2);
        ((BridgeHandler) handler2).setDest(receiver1);

//        receiver1.addFilter("logger", new LoggingFilter());
        receiver1.addFilter("codec", new ProtocolCodecFilter(new BridgeProtocolCodecFactory()));    // 设置编码过滤器
//        receiver2.addFilter("logger", new LoggingFilter());
        receiver2.addFilter("codec", new ProtocolCodecFilter(new BridgeProtocolCodecFactory()));    // 设置编码过滤器
        if (jToggleButton_Start.isSelected()) {
            try {
                receiver1.startListen();
                receiver2.startListen();
            } catch (RuntimeIoException e) {
                System.out.println("串口打开失败");
            }

            if (((BridgeHandler) handler1).outfile == null) {
                File path = new File("c:\\logs\\");
                path.mkdirs();
                ((BridgeHandler) handler1).outfile = new File("c:\\logs\\Out1-" + sdf.format(new Date()) + ".txt");
                ((BridgeHandler) handler2).outfile = new File("c:\\logs\\Out2-" + sdf.format(new Date()) + ".txt");
            }
            ((BridgeHandler) handler1).initfilelog();
            ((BridgeHandler) handler2).initfilelog();
            jToggleButton_Start.setText("关闭");
        } else {
            receiver1.endListen();
            receiver2.endListen();
            ((BridgeHandler) handler1).teminatefilelog();
            ((BridgeHandler) handler2).teminatefilelog();

            jToggleButton_Start.setText("开始");
        }

        java.util.logging.Logger.getLogger(SerialConnector.class.getName()).log(java.util.logging.Level.SEVERE, "XXXXX");
    }//GEN-LAST:event_jToggleButton_StartActionPerformed


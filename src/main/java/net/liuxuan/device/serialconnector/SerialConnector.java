    private SerialAddress getserialport1() {
        int ibaudrate = Integer.parseInt((String) jComboBox_BaudRate1.getSelectedItem());
        String port = (String) jComboBox_ComPort1.getSelectedItem();
        portAddress1 = new SerialAddress(port, ibaudrate, SerialAddress.DataBits.DATABITS_8, SerialAddress.StopBits.BITS_1, SerialAddress.Parity.NONE,
                SerialAddress.FlowControl.NONE);
        log.debug("port1:{}", portAddress1.toString());
        return portAddress1;
    }

    private SerialAddress getserialport2() {
        int ibaudrate = Integer.parseInt((String) jComboBox_BaudRate2.getSelectedItem());
        String port = (String) jComboBox_ComPort2.getSelectedItem();
        portAddress2 = new SerialAddress(port, ibaudrate, SerialAddress.DataBits.DATABITS_8, SerialAddress.StopBits.BITS_1, SerialAddress.Parity.NONE,
                SerialAddress.FlowControl.NONE);
        log.debug("port2:{}", portAddress1.toString());
        return portAddress2;
    }

